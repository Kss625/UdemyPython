'''  Arithmetic Operators'''
print(1 + 2)  # addition
print(1 - 6)  # subtraction
print(4 * 5)  # multiplication
print(5 / 4)  # Division gives value in Float
print(5 // 4)  # Division gives value in Integers
print(5 ** 2)  # power
print(10 % 3, 3 % 10)  # Remainder
print("Sushant" + "Kumar")  # allowed in string
print("Hello" * 3)  # print 3 times "Hello"
print([1, 2] + [3, 4])  # allowed in list
print([1, 2] * 2)  # allowed
print((1, 2) + (4, 6))  # allowed in tuple also
print((1, 2) * 5)  # allowed in tuple
# print({"name":"Sushant"}+{"Age":18}) not allowed in dictionary
